/*
** fp_uscale.c - Unrounded Scaling float conversion
**
** Unified float formatting and parsing using the uscale algorithm.
** Based on Russ Cox's "Unrounded Scaling" approach.
** See https://research.swtch.com/fp
**
** Replaces fmt_fp.c (formatting) and readfloat.c (parsing).
*/

