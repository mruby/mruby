/* ======== Platform support ======== */

#if defined(__SIZEOF_INT128__)
static inline void mul64(uint64_t a, uint64_t b, uint64_t *hi, uint64_t *lo)
{
  __uint128_t r = (__uint128_t)a * b;
  *hi = (uint64_t)(r >> 64);
  *lo = (uint64_t)r;
}
#elif defined(_MSC_VER) && defined(_M_X64)
#include <intrin.h>
static inline void mul64(uint64_t a, uint64_t b, uint64_t *hi, uint64_t *lo)
{
  *lo = _umul128(a, b, hi);
}
#else
static inline void mul64(uint64_t a, uint64_t b, uint64_t *hi, uint64_t *lo)
{
  uint64_t a_lo = (uint32_t)a, a_hi = a >> 32;
  uint64_t b_lo = (uint32_t)b, b_hi = b >> 32;
  uint64_t p0 = a_lo * b_lo;
  uint64_t p1 = a_lo * b_hi;
  uint64_t p2 = a_hi * b_lo;
  uint64_t p3 = a_hi * b_hi;
  uint64_t mid = (p0 >> 32) + (uint32_t)p1 + (uint32_t)p2;
  *lo = (p0 & 0xFFFFFFFFULL) | (mid << 32);
  *hi = p3 + (p1 >> 32) + (p2 >> 32) + (mid >> 32);
}
#endif

#if defined(__GNUC__) || defined(__clang__)
#define clz64(x) __builtin_clzll(x)
#elif defined(_MSC_VER) && defined(_M_X64)
static inline int clz64(uint64_t x)
{
  unsigned long idx;
  _BitScanReverse64(&idx, x);
  return 63 - (int)idx;
}
#else
static inline int clz64(uint64_t x)
{
  int n = 0;
  if (x <= 0x00000000FFFFFFFFULL) { n += 32; x <<= 32; }
  if (x <= 0x0000FFFFFFFFFFFFULL) { n += 16; x <<= 16; }
  if (x <= 0x00FFFFFFFFFFFFFFULL) { n +=  8; x <<=  8; }
  if (x <= 0x0FFFFFFFFFFFFFFFULL) { n +=  4; x <<=  4; }
  if (x <= 0x3FFFFFFFFFFFFFFFULL) { n +=  2; x <<=  2; }
  if (x <= 0x7FFFFFFFFFFFFFFFULL) { n +=  1; }
  return n;
}
#endif

#define bits_len64(x) (64 - clz64(x))

